package Day1;

public class primitivedata {
    static byte defaultByte;
    static short defaultShort;
    static int defaultInt;
    static long defaultLong;
    static float defaultFloat;
    static double defaultDouble;
    static char defaultChar;
    static boolean defaultBoolean;

    public static void main(String[] args) {

        System.out.println("defaultByte value is " + defaultByte);
        System.out.println("defaultShort value is " + defaultShort);
        System.out.println("defaultInt value is " + defaultInt);
        System.out.println("defaultLong value is " + defaultLong);
        System.out.println("defaultFloat value is " + defaultFloat);
        System.out.println("defaultDouble value is " + defaultDouble);
        System.out.println("defaultChar value is " + defaultChar);
        System.out.println("defaultBoolean value is " + defaultBoolean);
    }
}
